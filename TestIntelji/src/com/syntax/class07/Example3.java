package com.syntax.class07;

public class Example3 {

	public static void main(String[] args) {
		// print from 20 to 1
		int c = 20;
		while (c >= 1) {
			System.out.print(c + " ");// use System.out.print(c+" ") you can print on the same line
			c--;
		}
		System.out.println("the end of code");
	}

}
